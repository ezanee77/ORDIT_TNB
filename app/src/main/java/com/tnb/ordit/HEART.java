package com.tnb.ordit;

public class HEART {
    public String id, name, gender, age, heartRateValues, heartRateLevel;

    public HEART(String id, String name, String gender, String age, String heartRateValues, String heartRateLevel) {
        this.id = id;
        this.name = name;
        this.gender = gender;
        this.age = age;
        this.heartRateValues = heartRateValues;
        this.heartRateLevel = heartRateLevel;
    }
}
